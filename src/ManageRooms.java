    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
 String roomNumber = room_number1.getText();

    try {
        Connection con = ConnectDB.getCon();
        if (con != null) {
            PreparedStatement ps = con.prepareStatement("SELECT * FROM manage_room WHERE roomNumber = ?");
            ps.setString(1, roomNumber);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                // If the room is found, update UI fields accordingly
                if (rs.getString("isActive").equals("Yes")) {
                    check_activate_deactivate1.setSelected(true);
                } else {
                    check_activate_deactivate1.setSelected(false);
                }
                room_number1.setEditable(true);
                room_number1.setForeground(Color.BLACK);
                room_number1.setBackground(Color.WHITE);
            } else {
                
                JOptionPane.showMessageDialog(null, "Room Not Found");
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Failed to establish database connection");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_searchActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
           String roomNumber = room_number1.getText(); // Get the room number entered by the user
    String isActive = check_activate_deactivate1.isSelected() ? "Yes" : "No"; // Get the updated active status

    try {
        Connection con = ConnectDB.getCon();
        if (con != null) {
            PreparedStatement ps = con.prepareStatement("UPDATE manage_room SET isActive = ? WHERE roomNumber = ?");
            ps.setString(1, isActive); // Set the updated active status
            ps.setString(2, roomNumber); // Set the room number as a parameter
            int rowsUpdated = ps.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Room Updated Successfully");
                tableDetails(); // Refresh the table to reflect changes
                clear(); // Clear input fields
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Update Room");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Failed to establish database connection");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_updateActionPerformed


    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
   String roomNumberValue = room_number.getText();
    String isActive = check_active_deactive.isSelected() ? "Yes" : "No";
    String status = "NOT BOOKED";

    try {
        Connection con = ConnectDB.getCon();
        if (con != null) {
            // Check if the room number already exists
            PreparedStatement checkIfExists = con.prepareStatement("SELECT * FROM manage_room WHERE roomNumber = ?");
            checkIfExists.setString(1, roomNumberValue);
            ResultSet rs = checkIfExists.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Room Number Already Exists");
            } else {
                // If the room number doesn't exist, proceed with insertion
                PreparedStatement ps = con.prepareStatement("INSERT INTO manage_room VALUES (?, ?, ?)");
                ps.setString(1, roomNumberValue);
                ps.setString(2, isActive);
                ps.setString(3, status);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Successfully Added");
                tableDetails();
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Failed to establish database connection");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }



    }//GEN-LAST:event_btn_saveActionPerformed

